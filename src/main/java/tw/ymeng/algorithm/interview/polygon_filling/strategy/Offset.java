package tw.ymeng.algorithm.interview.polygon_filling.strategy;

class Offset {

    private final int x;
    private final int y;

    public Offset(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int x() {
        return x;
    }

    public int y() {
        return y;
    }
}
